    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // embed message
        String status = "Did not embed";
        String keyString = jTextField4.getText();
        String message = jTextArea1.getText();
        String inFile = jTextField1.getText();
        String outFile = jTextField2.getText();
        String size = jTextField3.getText();
        Embedder embed = new Embedder(inFile, outFile, keyString, message, size);
        embed.openFile(inFile);
        message = embed.encryption();
        try {
            status = embed.hide(message);
        } catch (IOException ex) {
            Logger.getLogger(StegoTag.class.getName()).log(Level.SEVERE, null, ex);
        }

        jTextField8.setText(status);

    }//GEN-LAST:event_jButton6ActionPerformed

