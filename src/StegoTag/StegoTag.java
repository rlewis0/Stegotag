    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // calculate maximum number of characters you can embed
            jTextField8.setText("");
            String input = jTextField1.getText();
            ArrayList text = new ArrayList();
            count = 0;
            String textLine;
            Scanner sf = new Scanner(new File(input));
            while (sf.hasNext()) {
                text.add(sf.nextLine());
            }
            sf.close();
            for (int x = 0; x < text.size(); x++) {
                textLine = (String) text.get(x);
                Scanner sc = new Scanner(textLine);
                sc.useDelimiter(">");
                while (sc.hasNext()) {
                    sc.next();
                    count++;
                }
            }
            count /= 8;
            String maxChar = "" + count;
            jTextField3.setText(maxChar);
            jButton1.setEnabled(true);

        } catch (FileNotFoundException ex) {
            jTextField8.setText("File not Found - Re-enter File Name ");
            jTextField1.setText("");
            jTextField1.grabFocus();
            //Logger.getLogger(StegoTag.class.getName()).log(Level.SEVERE, null, ex);

        }

    }//GEN-LAST:event_jButton3ActionPerformed

